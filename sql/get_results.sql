USE BIG_DATA; 

DROP TABLE IF EXISTS STATEWISE_RESULTS; 
CREATE TABLE STATEWISE_RESULTS (
	STATE VARCHAR(100),
	AVERAGE_NEET_MARK INT,
	POPULATION INT,
	GDP_PER_CAPITA DECIMAL(10,2),
	INCOME_PER_CAPITA DECIMAL(10,2),
	POVERTY DECIMAL(5,2),
	UNEMPLOYMENT DECIMAL(5,2),
	INTERNET_FACILITY_SCHOOLS DECIMAL(5,2),
	COMPUTER_FACILITY_SCHOOLS DECIMAL(5,2),
	ELECTRICITY_FACILITY_SCHOOLS DECIMAL(5,2),
	LIBRARY_FACILITY_SCHOOLS DECIMAL(5,2),
	TEACHERS_FACILITY_SCHOOLS DECIMAL(5,2),
	STUDENT_ENROLLMENT INT,
	PHONE_USAGE INT,
	PHONE_USAGE_EDUCATIONAL INT,
	PHONE_USAGE_NON_EDUCATIONAL INT,
	PRIMARY KEY(STATE)
);

DROP TABLE IF EXISTS NATIONAL_RESULTS; 
CREATE TABLE NATIONAL_RESULTS (
	AVERAGE_NEET_MARK INT,
	PHONE_USAGE INT,
);